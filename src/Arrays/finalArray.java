package src.Arrays;

public class finalArray {
    public static void main(String[] args) {
         final int[] a = new int[6];
         a[0]=98;
         a[1]=34;
        System.out.println(a[0]);
        System.out.println(a[1]);
//        here final is for length i.e 6 , we can change the values and assigned it to a[]
    }
}
