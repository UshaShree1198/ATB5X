package src.Basics001;

public class ArithmeticOperator {
    public static void main(String[] args) {

        int a = 60;
        int b = 56;

        int sum = a+b;
        System.out.println(sum);
        int sub = a-b;
        System.out.println(sub);
        int mul = a*b;
        System.out.println(mul);
        int div = a/b;
        System.out.println(div);


    }
}
