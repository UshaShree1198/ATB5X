package src.Basics001;

public class Concatination {
    public static void main(String[] args) {
        int a = 6;
        int b = 5;
        String name = "USHA" ;


        System.out.println(a+b+name);
        System.out.println(name+a+b);
        System.out.println((a+b)+name);
        System.out.println((a)+b+name);
        System.out.println(a+name+b);


    }
}
