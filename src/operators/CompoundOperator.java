package src.operators;

public class CompoundOperator {

    public static void main(String[] args) {

        int a= 10;
        int b = 40;
        b += 30;
        System.out.println(b);  // b= b+30 --> b=70

        int a1 = 90;
        a1 -= 70;
        System.out.println(a1);

//  compound operator -=,+=,/=,*=
    }
}
